@file:Suppress("unused")

package io.lettuce.core.api.coroutines.async

import kotlinx.coroutines.future.await
import io.lettuce.core.api.coroutines.*
import io.lettuce.core.sentinel.api.coroutines.*
import io.lettuce.core.api.async.*
import io.lettuce.core.sentinel.api.async.*
import io.lettuce.core.ExperimentalLettuceCoroutinesApi
import io.lettuce.core.TransactionResult


/**
 * Coroutine executed commands (based on async commands) for Transactions.
 * 
 * @param <K> Key type.
 * @param <V> Value type.
 * @author Mikhael Sokolov
 * @since 6.0
 * 
 * @generated by io.lettuce.apigenerator.CreateKotlinCoroutinesAsyncImplementation
 **/
@ExperimentalLettuceCoroutinesApi
internal class RedisTransactionalSuspendableAsyncCommands<K, V>(private val ops: RedisTransactionalAsyncCommands<K, V>) : RedisTransactionalSuspendableCommands<K, V> {

    /**
     * Discard all commands issued after MULTI.
     * 
     * @return String simple-string-reply always {@code OK}.
     * 
     **/
    override suspend fun discard(): String? = ops.discard().await()

    /**
     * Execute all commands issued after MULTI.
     * 
     * @return List<Object> array-reply each element being the reply to each of the commands in the atomic transaction.
     * 
     *         When using {@code WATCH}, {@code EXEC} can return a {@link TransactionResult#wasDiscarded discarded
     *         TransactionResult}.
     * @see TransactionResult#wasDiscarded
     * 
     **/
    override suspend fun exec(): TransactionResult? = ops.exec().await()

    /**
     * Mark the start of a transaction block.
     * 
     * @return String simple-string-reply always {@code OK}.
     * 
     **/
    override suspend fun multi(): String? = ops.multi().await()

    /**
     * Watch the given keys to determine execution of the MULTI/EXEC block.
     * 
     * @param keys the key.
     * @return String simple-string-reply always {@code OK}.
     * 
     **/
    override suspend fun watch(vararg keys: K?): String? = ops.watch(*keys).await()

    /**
     * Forget about all watched keys.
     * 
     * @return String simple-string-reply always {@code OK}.
     * 
     **/
    override suspend fun unwatch(): String? = ops.unwatch().await()

}

